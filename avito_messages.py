import random

# Список вариантов первого сообщения (нейтральные и короткие)
# Задача: просто получить ответ "Да", чтобы продолжить диалог.
avito_messages = [
    "Здравствуйте! Вы сейчас принимаете заказы на кровлю?",
    "Приветствую. Подскажите, по заборам в [CITY] еще работаете?",
    "Добрый день! Вы занимаетесь установкой заборов?",
    "Здравствуйте, подскажите, вы сейчас свободны для объектов по кровле?",
    "Добрый вечер. Увидел объявление, вы в [CITY] работаете сейчас?",
    "Привет! Подскажите, по кровле еще актуально объявление?",
    "Здравствуйте! Вы сами монтируете или бригада?",
    "Добрый день. А в области работаете (кровля/заборы)?",
    "Здравствуйте! Подскажите, какой сейчас график, заказы берете?",
    "Приветствую. Увидел ваше объявление по заборам. Работаете?",
    "Здравствуйте, вы сейчас берете новые объекты по кровле?",
    "Добрый день! Подскажите, актуально еще по установке заборов?",
    "Здравствуйте! А вы только в [CITY] работаете или по району тоже?",
    "Привет! Вы сейчас свободны для просчета объекта по кровле?"
]

def get_random_message(city):
    msg = random.choice(avito_messages)
    return msg.replace("[CITY]", city)

if __name__ == "__main__":
    # Тест генерации
    test_city = "Краснодар"
    print(f"Примеры сообщений для города {test_city}:")
    for _ in range(5):
        print(f"- {get_random_message(test_city)}")
